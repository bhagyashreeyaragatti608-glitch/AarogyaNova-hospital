function getPdfLibrary(){
  if(window.jspdf) return Promise.resolve(window.jspdf.jsPDF);
  return new Promise((resolve,reject)=>{
    const script=document.createElement('script');
    script.src='https://cdnjs.cloudflare.com/ajax/libs/jspdf/2.5.1/jspdf.umd.min.js';
    script.onload=()=>resolve(window.jspdf.jsPDF);
    script.onerror=()=>reject(new Error('PDF library could not be loaded.'));
    document.head.appendChild(script);
  });
}

async function downloadAppointmentPdf(result,data){
  const button=document.getElementById('downloadPdf');
  button.disabled=true;
  button.innerHTML='<i class="fa-solid fa-spinner fa-spin"></i> Preparing PDF…';
  try{
    const JsPDF=await getPdfLibrary();
    const pdf=new JsPDF({unit:'mm',format:'a4'});
    pdf.setFillColor(6,38,29); pdf.rect(0,0,210,42,'F');
    pdf.setTextColor(198,251,75); pdf.setFontSize(19); pdf.text('AAROGYANOVA HOSPITAL',18,20);
    pdf.setTextColor(255,255,255); pdf.setFontSize(10); pdf.text('Appointment prediction summary',18,30);
    pdf.setTextColor(22,34,29); pdf.setFontSize(17); pdf.text('Prediction outcome',18,60);
    pdf.setFontSize(12); pdf.text(`Appointment status: ${result.status}`,18,74);
    pdf.text(`Prediction confidence: ${result.confidence}%`,18,84);
    pdf.text(`Risk level: ${result.risk}`,18,94);
    pdf.setFontSize(13); pdf.text('Recommended action',18,113);
    pdf.setFontSize(11); pdf.text(pdf.splitTextToSize(result.recommendation,170),18,123);
    pdf.setFontSize(13); pdf.text('Appointment details',18,151);
    pdf.setFontSize(11); pdf.text(`Department: ${data.department}   •   Type: ${data.appointment_type}`,18,162);
    pdf.text(`City: ${data.city}   •   Severity: ${data.severity_level}`,18,172);
    pdf.setTextColor(95,108,100); pdf.setFontSize(9);
    pdf.text('Generated by AarogyaNova Hospital. This demo prediction is not medical advice.',18,280);
    pdf.save('aarogyanova-appointment-summary.pdf');
  }catch(error){
    alert('The PDF could not be created. Please check your internet connection and try again.');
  }finally{
    button.disabled=false;
    button.innerHTML='<i class="fa-solid fa-file-pdf"></i> Download my appointment PDF';
  }
}

form?.addEventListener('submit',event=>{
  event.preventDefault();
  if(!form.checkValidity()){form.classList.add('was-validated');form.reportValidity();return}
  const button=form.querySelector('button[type="submit"]');
  button.disabled=true;
  button.innerHTML='<i class="fa-solid fa-spinner fa-spin"></i> Analyzing appointment…';
  const data=Object.fromEntries(new FormData(form));
  // Replace this mock response with a fetch('/predict') request when a backend is connected.
  setTimeout(()=>{
    const missed=Number(data.missed_previous_appointments);
    const emergency=data.emergency_case==='Yes';
    const result=missed>1&&!emergency
      ?{status:'Cancelled',confidence:89.4,risk:'High',recommendation:'Contact the patient and offer a convenient reschedule.'}
      :{status:'Completed',confidence:emergency?98.2:96.8,risk:emergency?'Medium':'Low',recommendation:emergency?'Prioritize clinical triage and confirm attendance.':'Patient is likely to attend. Send a standard appointment reminder.'};
    document.getElementById('resultStatus').textContent=result.status;
    document.getElementById('resultConfidence').textContent=result.confidence+'%';
    document.getElementById('resultRisk').textContent=result.risk;
    document.getElementById('resultRecommendation').textContent=result.recommendation;
    const badge=document.getElementById('resultBadge');
    badge.textContent=result.risk+' risk';
    badge.style.background=result.risk==='High'?'#fde8e7':result.risk==='Medium'?'#fff2d8':'#dcf7ed';
    badge.style.color=result.risk==='High'?'#c94437':result.risk==='Medium'?'#a66300':'#108852';
    const card=document.getElementById('predictionResult');
    if(!document.getElementById('downloadPdf')){
      const pdfButton=document.createElement('button');
      pdfButton.type='button'; pdfButton.id='downloadPdf';
      pdfButton.className='btn btn-outline-primary pdf-download';
      pdfButton.innerHTML='<i class="fa-solid fa-file-pdf"></i> Download my appointment PDF';
      pdfButton.onclick=()=>downloadAppointmentPdf(result,data);
      card.appendChild(pdfButton);
    }else document.getElementById('downloadPdf').onclick=()=>downloadAppointmentPdf(result,data);
    card.classList.add('show'); card.scrollIntoView({behavior:'smooth',block:'center'});
    button.disabled=false;
    button.innerHTML='<span class="button-text">Predict appointment status</span> <i class="fa-solid fa-wand-magic-sparkles"></i>';
  },850);
});
